package com.demo.pokemon.model;

public class Camera {
	private String cylinderRadius;
	private String diskRadius;
	private String shoulderModeScale;

	public String getCylinderRadius() {
		return cylinderRadius;
	}

	public void setCylinderRadius(String cylinderRadius) {
		this.cylinderRadius = cylinderRadius;
	}

	public String getDiskRadius() {
		return diskRadius;
	}

	public void setDiskRadius(String diskRadius) {
		this.diskRadius = diskRadius;
	}

	public String getShoulderModeScale() {
		return shoulderModeScale;
	}

	public void setShoulderModeScale(String shoulderModeScale) {
		this.shoulderModeScale = shoulderModeScale;
	}

}
